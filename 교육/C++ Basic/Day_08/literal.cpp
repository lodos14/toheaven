/*
리터럴(Literal) :상수
- 숫자상수
 - 정수형 상수 : 10진수, 2진수, 8진수, 16진수

 - 실수형상수 : 소수점 이하 여섯자리까지
				4.2f, 4.2(double)

- 문자상수

변수(variable) : 상수가 저장되는 공간을 의미, 기억공간
	- 정수형변수
	- 실수형변수
	- 문자형변수
	- 문자형 변수를 표현하기 위해 배열을 사용한다.
	  char a = 'A';
	  char b = 'b';
	  char c = 'c';
	  char x[3] = {'A', 'B', 'C'};
	  char y[4] = "ABC" ; 반드시 1개 더 큰 공간 설정(null string)

데이터 타입(Data type) : 자료형
 - 변수와 상수의 크기를 규정해 높은 예약어
 - 기본자료형 과 사용자 정의 자료형
 - 기본자료형 : 정수형 : char, short, int, long, long long.....
				실수형 : float(4), double(8), long double(8)
				문자형 : char(1) => ASCII 코드-통신(7bits)
						 A(65) - Z(90), a(97)~z(122)

*/